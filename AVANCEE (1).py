import ipywidgets as wd
from IPython.display import display
import pandas as pd
from google.colab import files

#titulo y estilo de los campos de entrada
titulo = wd.HTML("<h1>INVENTARIO FORESTAL</h1>")
style = {'description_width': '130px'}

#Listas
datos_fauna = []
datos_flora = []

#botones iniciales
botonFauna = wd.Button(description = "FAUNA", button_style = "primary")
botonFlora = wd.Button(description = "FLORA", button_style = 'success')
botonDatos = wd.Button(description = "MOSTRAR DATOS", button_style = "info")
f1 = wd.HBox([botonFauna, botonFlora, botonDatos])

#botones para cuando haya algun dato guardado
botonEditar = wd.Button(description = "EDITAR")
botonEliminar = wd.Button(description = "ELIMINAR", button_style = "danger")
botonDescargar = wd.Button(description = "DESCARGAR", button_style = "success")
#ocultar estos tres botones desde el principio
f2 = wd.HBox([botonEditar, botonEliminar, botonDescargar])
f2.layout.display = 'none'

#caja de salida
salida = wd.Output()

#funciones
def mostrarBotones():
  if len(datos_fauna) > 0 or len(datos_flora) > 0:
    f2.layout.display = ''
  else:
    f2.layout.display = 'none'

def fauna(b):
    salida.clear_output()
    with salida:
      print("-"*15,"Animales","-"*15)
      brigada = wd.IntText(description="Número# Brigada:", style = style)
      conglomerado = wd.Dropdown(description="Conglomerado:", options=["Caribe", "Andina", "Pacífico", "Orinoquía", "Amazonas"], style = style)
      codigoId = wd.IntText(description="Código ID:", style = style)
      nombre = wd.Text(description="Nombre común:", style = style)
      cantidad = wd.IntText(description = "Cantidad:", style = style)
      nombreCientifico = wd.Text(description="Nombre científico:", style = style)
      tipoAnimal = wd.Dropdown(description="Tipo de animal:", options=["Mamífero","Ave", "Pez", "Reptil", "Anfibio","Invertebrado","Otro"], style = style)

      #boton para guardar los datos
      botonGuardar = wd.Button(description="Guardar", button_style="warning")
      mensaje = wd.Output()

      def guardar_fauna(b):
        mensaje.clear_output()
        with mensaje:
          if brigada.value == 0 or codigoId.value == 0 or nombre.value == '' or cantidad.value == '' or nombreCientifico.value == '':
            print(f"Ingrese valores en todos los campos para continuar.")
            return
          else:
            datos_fauna.append({
                "Brigada": brigada.value,
                "Conglomerado": conglomerado.value,
                "Id": codigoId.value,
                "Nombre": nombre.value,
                "Nombre_cientifico": nombreCientifico.value,
                "Cantidad": cantidad.value,
                "Tipo": tipoAnimal.value
            })

            #limpiar los campos de entrada después de guardar
            brigada.value = 0
            codigoId.value = 0
            nombre.value = ''
            cantidad.value = 0
            nombreCientifico.value = ''
            mostrarBotones()
            print(f"Ha sido guardado correctamente.")

      botonGuardar.on_click(guardar_fauna)
      display(wd.VBox([brigada, conglomerado, codigoId, nombre, nombreCientifico, tipoAnimal, cantidad, botonGuardar, mensaje]))


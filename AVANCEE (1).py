def mostrarDatos(b):
  salida.clear_output()
  with salida:
    if len(datos_fauna) == 0 and len(datos_flora) == 0:
      print("No hay datos en fauna ni en flora.")
      return
    #fauna
    if len(datos_fauna) > 0:
      display(wd.HTML("<h1>Datos Fauna</h1>"))
      display(pd.DataFrame(datos_fauna))
    else:
      print("\nNo hay datos de fauna.")
    #flora
    if len(datos_flora) > 0:
      display(wd.HTML("<h1>Datos Flora</h1>"))
      display(pd.DataFrame(datos_flora))
    else:
      print("\nNo hay datos de flora.")

def editarDatos(b):
  salida.clear_output()
  with salida:
      print("\nIngrese el Código ID del registro a editar:")
      id = wd.IntText(description="Código ID a editar:", style=style)
      botonBuscar = wd.Button(description="BUSCAR", button_style="info")
      mensaje = wd.Output()

      def buscar_registro(b):
        mensaje.clear_output()
        with mensaje:
          #Buscar en fauna
          for i, registro in enumerate(datos_fauna):
            if registro["Id"] == id.value:
              print("Id encontrado....")
              return

          #Buscar en flora
          for i, registro in enumerate(datos_flora):
            if registro["Id"] == id.value:
              print("Id encontrado....")
              return

      botonBuscar.on_click(buscar_registro)
      display(wd.VBox([id, botonBuscar, mensaje]))

def eliminar(b):
  salida.clear_output()
  with salida:
    print("\nIngrese el ID del registro a eliminar:")
    id = wd.IntText(description = "Codigo ID a eliminar:", style = style)
    botonBuscar = wd.Button(description = "BUSCAR", button_style = "info")
    mensaje = wd.Output()

    def buscarId(b):
      mensaje.clear_output()
      with mensaje:
        if id.value == 0:
          print("\nIngrese el ID para continuar.")
          return

        #eliminar un registro guardado en fauna
        for i, registro in enumerate(datos_fauna):
          if registro["Id"] == id.value:
            borrar = datos_fauna.pop(i)
            mostrarBotones()
            print("Su registro ha sido borrado correctamente.")
            return

        #elminiar un registro guardado en flora
        for i, registro in enumerate(datos_flora):
          if registro["Id"] == id.value:
            borrar = datos_flora.pop(i)
            mostrarBotones()
            print(f"Su registro con codigo ID {borrar} ha sido eliminado correctamente.")
            return

        print(f"ID: {id.value} no econtrado, verifique y vuelva a intentar.")
        id.value = 0

    botonBuscar.on_click(buscarId)
    display(wd.VBox([id,botonBuscar, mensaje]))

botonEliminar.on_click(eliminar)
botonFauna.on_click(fauna)
botonFlora.on_click(flora)
botonDatos.on_click(mostrarDatos)
botonEditar.on_click(editarDatos)

display(titulo, f1, f2, salida)
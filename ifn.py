def eliminar(b):
  salida.clear_output()
  with salida:
    print("\nIngrese el ID del registro a eliminar:")
    print()
    id = wd.IntText(description = "Codigo ID a eliminar:", style = style)
    botonBuscar = wd.Button(description = "BUSCAR", button_style = "info")
    mensaje = wd.Output()

    def buscarId(b):
      mensaje.clear_output()
      with mensaje:
        if id.value == 0:
          print("Ingrese el ID para continuar.")
          return

        #eliminar un registro guardado en fauna
        for i, registro in enumerate(datos_fauna):
          if registro["Id"] == id.value:
            borrar = datos_fauna.pop(i)
            mostrarBotones()
            print(f"Su registro con codigo ID: {registro['Id']} ha sido eliminado correctamente.")
            print("\nRegistro elminado:")
            display(pd.DataFrame([borrar]))
            return

        #elminiar un registro guardado en flora
        for i, registro in enumerate(datos_flora):
          if registro["Id"] == id.value:
            borrar = datos_flora.pop(i)
            mostrarBotones()
            print(f"Su registro con codigo ID: {registro['Id']} ha sido eliminado correctamente.")
            print("\nRegistro elminado:")
            display(pd.DataFrame([borrar]))
            return

        print(f"ID: {id.value} no econtrado. Verifique el ID en Mostrar datos.")
        id.value = 0

    botonBuscar.on_click(buscarId)
    display(wd.VBox([id,botonBuscar, mensaje]))


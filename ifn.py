def fauna(b):
    salida.clear_output()
    with salida:
      print("-"*15,"Animales","-"*15)
      #campos de entrada
      brigada = wd.IntText(description="Número Brigada:", style = style)
      conglomerado = wd.Dropdown(description="Conglomerado:", options=["Escoge una opción","Caribe", "Andina", "Pacífico", "Orinoquía", "Amazonas"], style = style)
      codigoId = wd.IntText(description="Código ID:", style = style)
      nombre = wd.Text(description="Nombre común:", style = style)
      cantidad = wd.IntText(description = "Cantidad:", style = style)
      nombreCientifico = wd.Text(description="Nombre científico:", style = style)
      tipoAnimal = wd.Dropdown(description="Tipo de animal:", options=["Escoge una opción", "Mamífero","Ave", "Pez", "Reptil", "Anfibio","Invertebrado","Otro"], style = style)

      #boton para guardar los datos
      botonGuardar = wd.Button(description="Guardar", button_style="warning")
      mensaje = wd.Output()

      def guardar_fauna(b):
        mensaje.clear_output()
        with mensaje:

          #validaciones de ID unico en datos(fauna y flora)
          #validacion en fauna
          for registro in datos_fauna:
            if registro["Id"] == codigoId.value:
              print(f"El ID: {codigoId.value} ya existe en fauna, asigne un ID único.")
              codigoId.value = 0
              return
          #validacion en flora
          for registro in datos_flora:
            if registro["Id"] == codigoId.value:
              print(f"El ID: {codigoId.value} ya existe en flora, asigne un ID único.")
              codigoId.value = 0
              return

          if brigada.value == 0 or codigoId.value == 0 or nombre.value == '' or nombreCientifico.value == '':
            print(f"Ingrese valores en todos los campos para continuar.")
            return
          elif brigada.value <= 0 or codigoId.value < 0 or cantidad.value <= 0:
            print(f"Ingrese valores positivos para continuar")
            return
          elif conglomerado.value == "Escoge una opción" or tipoAnimal.value == "Escoge una opción":
            print("Por favor seleccione una opción en cada una de las litas desplegables.")
            return
          else:
            datos_fauna.append({
                "Brigada": brigada.value,
                "Conglomerado": conglomerado.value,
                "Id": codigoId.value,
                "Nombre": nombre.value,
                "Nombre_cientifico": nombreCientifico.value,
                "Cantidad": cantidad.value,
                "Tipo": tipoAnimal.value
            })

            #limpiar los campos de entrada después de guardar
            brigada.value = 0
            codigoId.value = 0
            nombre.value = ''
            cantidad.value = 0
            nombreCientifico.value = ''
            mostrarBotones()
            print(f"Ha sido guardado correctamente.")

      botonGuardar.on_click(guardar_fauna)
      display(wd.VBox([brigada, conglomerado, codigoId, nombre, nombreCientifico, tipoAnimal, cantidad, botonGuardar, mensaje]))

